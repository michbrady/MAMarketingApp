IF NOT EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('dbo.Contact') AND name = 'NextFollowUpDate')
BEGIN
    ALTER TABLE dbo.Contact
    ADD NextFollowUpDate DATETIME2(7) NULL;

    CREATE NONCLUSTERED INDEX IX_Contact_NextFollowUp ON dbo.Contact(OwnerUserID, NextFollowUpDate)
        WHERE NextFollowUpDate IS NOT NULL AND Status = 'Active';
