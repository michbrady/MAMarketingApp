/*******************************************************************************
 * 1. FOLLOW-UP TEMPLATES
 ******************************************************************************/

CREATE TABLE dbo.FollowUpTemplate (
    TemplateID INT IDENTITY(1,1) NOT NULL,
    TemplateName NVARCHAR(100) NOT NULL,
    Description NVARCHAR(500) NULL,

    -- Default Settings
    DefaultDays INT NOT NULL DEFAULT 3,              -- Days after initial contact
    DefaultPriority NVARCHAR(20) NOT NULL DEFAULT 'Medium', -- Low, Medium, High, Urgent
    DefaultType NVARCHAR(20) NOT NULL DEFAULT 'Call', -- Call, Email, Meeting, Task, Other

    SuggestedAction NVARCHAR(MAX) NULL,              -- Suggested notes/action for UFO

    -- Template Category
    Category NVARCHAR(50) NULL,                      -- Product, Business, Event, NoResponse, ThankYou

    -- Status
    IsActive BIT NOT NULL DEFAULT 1,

    -- Audit
    CreatedDate DATETIME2(7) NOT NULL DEFAULT SYSDATETIME(),
    UpdatedDate DATETIME2(7) NOT NULL DEFAULT SYSDATETIME(),

    CONSTRAINT PK_FollowUpTemplate PRIMARY KEY CLUSTERED (TemplateID),
    CONSTRAINT UQ_FollowUpTemplate_Name UNIQUE (TemplateName)
);
GO

CREATE NONCLUSTERED INDEX IX_FollowUpTemplate_Active ON dbo.FollowUpTemplate(IsActive, Category);
GO

/*******************************************************************************
 * 2. FOLLOW-UP TASKS
 ******************************************************************************/

CREATE TABLE dbo.FollowUp (
    FollowUpID BIGINT IDENTITY(1,1) NOT NULL,
    UserID BIGINT NOT NULL,                          -- UFO who needs to follow up
    ContactID BIGINT NOT NULL,                       -- Contact to follow up with

    -- Scheduling
    DueDate DATETIME2(7) NOT NULL,
    Priority NVARCHAR(20) NOT NULL DEFAULT 'Medium', -- Low, Medium, High, Urgent

    -- Status
    Status NVARCHAR(20) NOT NULL DEFAULT 'Pending',  -- Pending, Completed, Snoozed, Cancelled

    -- Type and Details
    Type NVARCHAR(20) NOT NULL DEFAULT 'Call',       -- Call, Email, Meeting, Task, Other
    Notes NVARCHAR(MAX) NULL,                        -- UFO's notes about what to do

    -- Completion
    CompletedDate DATETIME2(7) NULL,
    CompletedNotes NVARCHAR(MAX) NULL,               -- What happened when completed

    -- Snooze Tracking
    SnoozedUntil DATETIME2(7) NULL,
    SnoozedCount INT NOT NULL DEFAULT 0,

    -- Related Entities
    RelatedShareEventID BIGINT NULL,                 -- If created from a share
    RelatedContentItemID BIGINT NULL,                -- Content that was shared
    CreatedFromTemplateID INT NULL,                  -- If created from template

    -- Automation
    IsAutoGenerated BIT NOT NULL DEFAULT 0,          -- True if system-created

    -- Reminders
    ReminderSent BIT NOT NULL DEFAULT 0,
    ReminderSentDate DATETIME2(7) NULL,

    -- Audit
    CreatedDate DATETIME2(7) NOT NULL DEFAULT SYSDATETIME(),
    UpdatedDate DATETIME2(7) NOT NULL DEFAULT SYSDATETIME(),

    CONSTRAINT PK_FollowUp PRIMARY KEY CLUSTERED (FollowUpID),
    CONSTRAINT FK_FollowUp_User FOREIGN KEY (UserID) REFERENCES dbo.[User](UserID),
    CONSTRAINT FK_FollowUp_Contact FOREIGN KEY (ContactID) REFERENCES dbo.Contact(ContactID),
    CONSTRAINT FK_FollowUp_ShareEvent FOREIGN KEY (RelatedShareEventID) REFERENCES dbo.ShareEvent(ShareEventID),
    CONSTRAINT FK_FollowUp_ContentItem FOREIGN KEY (RelatedContentItemID) REFERENCES dbo.ContentItem(ContentItemID),
    CONSTRAINT FK_FollowUp_Template FOREIGN KEY (CreatedFromTemplateID) REFERENCES dbo.FollowUpTemplate(TemplateID),
    CONSTRAINT CK_FollowUp_Priority CHECK (Priority IN ('Low', 'Medium', 'High', 'Urgent')),
    CONSTRAINT CK_FollowUp_Status CHECK (Status IN ('Pending', 'Completed', 'Snoozed', 'Cancelled')),
    CONSTRAINT CK_FollowUp_Type CHECK (Type IN ('Call', 'Email', 'Meeting', 'Task', 'Other'))
);
GO

CREATE NONCLUSTERED INDEX IX_FollowUp_User_Status ON dbo.FollowUp(UserID, Status, DueDate);
CREATE NONCLUSTERED INDEX IX_FollowUp_User_DueDate ON dbo.FollowUp(UserID, DueDate) WHERE Status = 'Pending';
CREATE NONCLUSTERED INDEX IX_FollowUp_Contact ON dbo.FollowUp(ContactID, Status, DueDate);
CREATE NONCLUSTERED INDEX IX_FollowUp_Priority ON dbo.FollowUp(UserID, Priority, DueDate) WHERE Status = 'Pending';
CREATE NONCLUSTERED INDEX IX_FollowUp_Overdue ON dbo.FollowUp(UserID, DueDate)
    WHERE Status = 'Pending' AND DueDate < SYSDATETIME();
CREATE NONCLUSTERED INDEX IX_FollowUp_ShareEvent ON dbo.FollowUp(RelatedShareEventID) WHERE RelatedShareEventID IS NOT NULL;
CREATE NONCLUSTERED INDEX IX_FollowUp_Reminder ON dbo.FollowUp(ReminderSent, DueDate)
    WHERE Status = 'Pending' AND ReminderSent = 0;
GO

/*******************************************************************************
 * 3. FOLLOW-UP REMINDERS
 ******************************************************************************/

CREATE TABLE dbo.FollowUpReminder (
    ReminderID BIGINT IDENTITY(1,1) NOT NULL,
    FollowUpID BIGINT NOT NULL,

    -- Reminder Details
    ReminderDate DATETIME2(7) NOT NULL,              -- When to send reminder
    DeliveryChannel NVARCHAR(20) NOT NULL DEFAULT 'InApp', -- InApp, Email, SMS, Push

    -- Status
    Sent BIT NOT NULL DEFAULT 0,
    SentDate DATETIME2(7) NULL,
    DeliveryStatus NVARCHAR(20) NULL,                -- Pending, Sent, Failed
    FailureReason NVARCHAR(500) NULL,

    -- Audit
    CreatedDate DATETIME2(7) NOT NULL DEFAULT SYSDATETIME(),

    CONSTRAINT PK_FollowUpReminder PRIMARY KEY CLUSTERED (ReminderID),
    CONSTRAINT FK_FollowUpReminder_FollowUp FOREIGN KEY (FollowUpID) REFERENCES dbo.FollowUp(FollowUpID) ON DELETE CASCADE
);
GO

CREATE NONCLUSTERED INDEX IX_FollowUpReminder_FollowUp ON dbo.FollowUpReminder(FollowUpID);
CREATE NONCLUSTERED INDEX IX_FollowUpReminder_Pending ON dbo.FollowUpReminder(ReminderDate, Sent)
    WHERE Sent = 0;
GO

