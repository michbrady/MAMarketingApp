-- Get Upcoming Follow-ups for a User
CREATE OR ALTER PROCEDURE dbo.sp_GetUpcomingFollowUps
    @UserID BIGINT,
    @Days INT = 7
AS
BEGIN
    SET NOCOUNT ON;

    SELECT
        f.FollowUpID,
        f.UserID,
        f.ContactID,
        c.FirstName + ' ' + ISNULL(c.LastName, '') as ContactName,
        c.Email as ContactEmail,
        c.Mobile as ContactMobile,
        f.DueDate,
        f.Priority,
        f.Status,
        f.Type,
        f.Notes,
        f.CompletedDate,
        f.CompletedNotes,
        f.SnoozedUntil,
        f.SnoozedCount,
        f.ReminderSent,
        f.ReminderSentDate,
        f.CreatedDate,
        f.UpdatedDate,
        CASE
            WHEN f.DueDate < SYSDATETIME() THEN 1
            ELSE 0
        END as IsOverdue
    FROM dbo.FollowUp f
    INNER JOIN dbo.Contact c ON f.ContactID = c.ContactID
    WHERE
        f.UserID = @UserID
        AND f.Status = 'Pending'
        AND f.DueDate <= DATEADD(DAY, @Days, SYSDATETIME())
    ORDER BY f.DueDate ASC, f.Priority DESC;
END
GO

-- Get Overdue Follow-ups
CREATE OR ALTER PROCEDURE dbo.sp_GetOverdueFollowUps
    @UserID BIGINT
AS
BEGIN
    SET NOCOUNT ON;

    SELECT
        f.FollowUpID,
        f.UserID,
        f.ContactID,
        c.FirstName + ' ' + ISNULL(c.LastName, '') as ContactName,
        c.Email as ContactEmail,
        c.Mobile as ContactMobile,
        f.DueDate,
        f.Priority,
        f.Status,
        f.Type,
        f.Notes,
        f.CompletedDate,
        f.CompletedNotes,
        f.SnoozedUntil,
        f.SnoozedCount,
        f.ReminderSent,
        f.ReminderSentDate,
        f.CreatedDate,
        f.UpdatedDate,
        DATEDIFF(HOUR, f.DueDate, SYSDATETIME()) as HoursOverdue
    FROM dbo.FollowUp f
    INNER JOIN dbo.Contact c ON f.ContactID = c.ContactID
    WHERE
        f.UserID = @UserID
        AND f.Status = 'Pending'
        AND f.DueDate < SYSDATETIME()
    ORDER BY f.Priority DESC, f.DueDate ASC;
END
GO

-- Create Automated Follow-up after Share
CREATE OR ALTER PROCEDURE dbo.sp_CreateAutomatedFollowUp
    @ShareEventID BIGINT,
    @ContactID BIGINT,
    @UserID BIGINT,
    @ContentItemID BIGINT,
    @DefaultDays INT = 3
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @DueDate DATETIME2(7) = DATEADD(DAY, @DefaultDays, SYSDATETIME());

    INSERT INTO dbo.FollowUp (
        UserID,
        ContactID,
        DueDate,
        Priority,
        Status,
        Type,
        Notes,
        RelatedShareEventID,
        RelatedContentItemID,
        IsAutoGenerated,
        CreatedDate,
        UpdatedDate
    )
    VALUES (
        @UserID,
        @ContactID,
        @DueDate,
        'Medium',
        'Pending',
        'Call',
        'Follow up on shared content',
        @ShareEventID,
        @ContentItemID,
        1,
        SYSDATETIME(),
        SYSDATETIME()
    );

    DECLARE @FollowUpID BIGINT = SCOPE_IDENTITY();

    -- Update Contact's NextFollowUpDate
    UPDATE dbo.Contact
    SET NextFollowUpDate = @DueDate,
        UpdatedDate = SYSDATETIME()
    WHERE ContactID = @ContactID;

    -- Create reminder for 24 hours before
    INSERT INTO dbo.FollowUpReminder (
        FollowUpID,
        ReminderDate,
        DeliveryChannel,
        CreatedDate
    )
    VALUES (
        @FollowUpID,
        DATEADD(HOUR, -24, @DueDate),
        'InApp',
        SYSDATETIME()
    );

    SELECT @FollowUpID as FollowUpID;
END
GO

-- Update Contact NextFollowUpDate Trigger
CREATE OR ALTER TRIGGER tr_FollowUp_UpdateContactNextFollowUp
ON dbo.FollowUp
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
    SET NOCOUNT ON;

    -- Get affected contacts
    DECLARE @AffectedContacts TABLE (ContactID BIGINT);

    INSERT INTO @AffectedContacts
    SELECT DISTINCT ContactID FROM inserted
    UNION
    SELECT DISTINCT ContactID FROM deleted;

    -- Update NextFollowUpDate for each affected contact
    UPDATE c
    SET NextFollowUpDate = (
        SELECT MIN(DueDate)
        FROM dbo.FollowUp
        WHERE ContactID = c.ContactID
            AND Status = 'Pending'
    ),
    UpdatedDate = SYSDATETIME()
    FROM dbo.Contact c
    WHERE c.ContactID IN (SELECT ContactID FROM @AffectedContacts);
END
GO

